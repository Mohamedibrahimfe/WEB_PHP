            <h2>Nobel Prize for Literature</h2>

            <p>
                <b>Mahfouz was awarded the 1988 Nobel Prize in Literature, the only Arab writer to have won the
                    award.</b>
                Shortly after winning the prize Mahfouz was quoted as saying:

                <b>The Nobel Prize has given me, for the first time in my life, the feeling that my literature could be
                    appreciated on an international level.
                    The Arab world also won the Nobel with me. I believe that international doors have opened, and that
                    from now on,
                    literate people will consider Arab literature also. We deserve that recognition.</b>

                The Swedish letter to Mahfouz praised his "rich and complex work":

                [It] invites us to reconsider the fundamental things in life. Themes like the nature of time and love,
                society and norms,
                knowledge and faith recur in a variety of situations and are presented in thought-provoking, evocative,
                and clearly daring ways.
                And the poetic quality of your prose can be felt across the language barrier. In the prize citation you
                are credited with the forming of an Arabian narrative art that applies to all mankind.

                <b>Because Mahfouz found traveling to Sweden difficult at his age, he did not attend the award
                    ceremony.</b>
