                in the Bayt al-Qadi neighborhood in the Gamaleya quarter in the old city,
                from where they moved in 1924 to Abbaseya, then a new Cairo suburb north of the old city, locations that
                would provide the backdrop for many of Mahfouz's later writings.
                His father, Abdel-Aziz Ibrahim,<b> whom Mahfouz described as having been "old-fashioned"</b>, was a
                civil servant, and Mahfouz eventually followed in his footsteps in 1934.
                <b>Mahfouz's mother, Fatimah, was the daughter of Mustafa Qasheesha, an Al-Azhar sheikh</b>, and
                although illiterate herself, took the boy Mahfouz on numerous excursions to cultural locations
                such as the Egyptian Museum and the Pyramids.
