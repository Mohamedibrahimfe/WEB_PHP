        <section class="text7">

            <h2>Writing style and themes</h2>
            <p>
                Most of Mahfouz's early works were set in Cairo. Abath Al-Aqdar (Mockery of the Fates) (1939),
                Rhadopis (1943), and Kifah Tibah (The Struggle of Thebes) (1944) were historical novels written as part
                of a larger unfulfilled 30-novel project.
                Inspired by Sir Walter Scott (1771–1832), Mahfouz planned to cover the entire history of Egypt in a
                series of books. However,
                following the third volume, his interest shifted to current settings and issues, as well as the
                psychological impact of social change on ordinary people.
            </p>

            <p>
                Mahfouz's works often deal with Egypt's development during the 20th century, and combined intellectual
                and cultural influences from both East and West.
                <b>His own exposure to foreign literature began in his youth with the enthusiastic consumption of
                    Western detective stories, Russian classics, and modernist writers as Marcel Proust, Franz Kafka and
                    James Joyce.</b>
                Mahfouz's stories are almost always set in the heavily populated urban quarters of Cairo, where his
                characters, usually ordinary people, try to cope with the modernization of society and the temptations
                of Western values.
                <br>
                <b> Mahfouz's central work in the 1950s was the Cairo Trilogy, which he completed before the July
                    Revolution.
                    The novels were titled with the street names Palace Walk, Palace of Desire, and Sugar Street.</b>
                Mahfouz set the story in the parts of Cairo where he grew up. The novels depict the life of the
                patriarch el-Sayyed Ahmed Abdel Gawad and his family over three generations,
                from World War I to the 1950s, when King Farouk I was overthrown.<b> Mahfouz stopped writing for some
                    years after finishing the trilogy.</b>
            </p>
        </section>
        <br>

        <section class="text7">
            <p>
                His 1966 novel Tharthara Fawq Al-Nīl (Adrift on the Nile) is one of his most popular works.
                It was later made into a film called Chitchat on the Nile during the régime of Anwar al-Sadat.
                The story criticizes the decadence of Egyptian society during the Nasser era. It was banned by Sadat to
                avoid provoking Egyptians who still loved former president Nasser.
                Copies of the banned book were hard to find prior to the late 1990s.

                The Children of Gebelawi (1959, also known as Children of the Alley) one of Mahfouz's best known works,
                portrayed the patriarch Gebelaawi and his children, average Egyptians living the lives of Cain and Abel,
                Moses, Jesus, and Mohammed.
                Gebelawi builds a mansion in an oasis in the middle of a barren desert; his estate becomes the scene of
                a family feud that continues for generations.
                "Whenever someone is depressed, suffering or humiliated, he points to the mansion at the top of the
                alley at the end opening out to the desert, and says sadly,
                'That is our ancestor's house, we are all his children, and we have a right to his property. Why are we
                starving? What have we done?'"
                The book was banned throughout the Arab world except in Lebanon until 2006 when it was first published
                in Egypt.
                The work was prohibited because of its alleged blasphemy through the allegorical portrayal of God and
                the monotheistic Abrahamic faiths of Judaism, Christianity, and Islam.

                In the 1960s, Mahfouz further developed the theme that humanity is moving further away from God in his
                existentialist novels.
                In The Thief and the Dogs (1961) he depicted the fate of a Marxist thief who has been released from
                prison and plans revenge.
            </p>

            <p>
                In the 1960s and 1970s Mahfouz began to construct his novels more freely and often used interior
                monologues.
                In Miramar (1967) he employed a form of multiple First-person narratives. Four narrators, among them a
                Socialist and a Nasserite opportunist, represent different political views.
                In the center of the story is an attractive servant girl. In Arabian Nights and Days (1979) and in The
                Journey of Ibn Fatouma (1983) he drew on traditional Arabic narratives as subtexts.
                Akhenaten: Dweller in Truth (1985) deals with conflict between old and new religious truths.

                <b> Many of his novels were first published in serialized form, including Children of Gebelawi and Midaq
                    Alley which was also adapted into a Mexican film starring Salma Hayek called El callejón de los
                    milagros.</b>
            </p>
