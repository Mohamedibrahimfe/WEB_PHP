                This one went to visit people, that one invited people. I had the impression that married life would
                take up all my time.
                I saw myself drowning in visits and parties. No freedom.

                However, in 1954, he quietly married a Coptic Orthodox woman from Alexandria, Atiyyatallah Ibrahim, with
                whom he had two daughters, Fatima and Umm Kalthum.
                The couple initially lived on a houseboat in the Agouza section of Cairo on the west bank of the Nile,
                then moved to an apartment along the river in the same area.
                Mahfouz avoided public exposure, especially inquiries into his private life, which might have become, as
                he put it, "a silly topic in journals and radio programs.

                Mahfouz distinctly did not like to travel. Belgrade was one of the few cities to which he gladly went
