                    <h1>
                        Naguib Mahfouz Abdelaziz Ibrahim Ahmed Al-Basha11 December 1911 – 30 August 2006) was an
                        Egyptian writer who won the 1988 Nobel Prize in Literature.
                        Mahfouz is regarded as one of the first contemporary writers in Arabic literature, along
                        with Taha Hussein, to explore themes of existentialism.
                        [1] He is the only Egyptian to win the Nobel Prize in Literature.
                        He published 35 novels, over 350 short stories, 26 screenplays, hundreds of op-ed columns
                        for Egyptian newspapers, and seven plays over a 70-year career, from the 1930s until 2004.
                        All of his novels take place in Egypt, and always mentions the lane, which equals the world.
                        His most famous works include The Cairo Trilogy and Children of Gebelawi.
                        Many of Mahfouz's works have been made into Egyptian and foreign films; no Arab writer
                        exceeds Mahfouz in number of works that have been adapted for cinema and television.
                        While Mahfouz's literature is classified as realist literature, existential themes appear in
                        it.
                    </h1>
                    <p>
                        Early life and education
                    </p>
                    <h1>
                        Mahfouz was born in a lower middle-class Muslim Egyptian family in Old Cairo in 1911.
                        The first part of his compound given name was chosen in appreciation of the well-known
                        obstetrician, Naguib Pasha Mahfouz, who oversaw his difficult birth.
                        [3] Mahfouz was the seventh and the youngest child, with four brothers and two sisters, all
                        of them much older than him.
                        (Experientially, he grew up an "only child".) The family lived in two popular districts of
                        Cairo: first, in the Bayt al-Qadi neighborhood in the Gamaleya quarter in the old city, from
                        where they moved in 1924 to Abbaseya, then a new Cairo suburb north of the old city,
                        locations that would provide the backdrop for many of Mahfouz's later writings. His father,
                        Abdel-Aziz Ibrahim, whom Mahfouz described as having been "old-fashioned", was a civil
                        servant, and Mahfouz eventually followed in his footsteps in 1934.
                        Mahfouz's mother, Fatimah, was the daughter of Mustafa Qasheesha, an Al-Azhar sheikh, and
                        although illiterate herself, took the boy Mahfouz on numerous excursions to cultural
                        locations such as the Egyptian Museum and the Pyramids.
                        The Mahfouz family were devout Muslims and Mahfouz had a strict Islamic upbringing. In an
                        interview, he elaborated on the stern religious climate at home during his childhood. He
                        stated, "You would never have thought that an artist would emerge from that family."
                        The Egyptian Revolution of 1919 had a strong effect on Mahfouz, although he was at the time
                        only seven years old.
                        From the window he saw British soldiers firing at the demonstrators in an effort to disperse
                        them.
                        According to Mahfouz, "You could say ... that the one thing which most shook the security of
                        my childhood was the 1919 revolution", he later said.
                        In his early years, Mahfouz read extensively and was influenced by Hafiz Najib, Taha Hussein
                        and Salama Moussa, the Fabian intellectual.
                        After completing his secondary education, Mahfouz was admitted in 1930 to the Egyptian
                        University (now Cairo University), where he studied philosophy, graduating in 1934.
                        By 1936, having spent a year working on an M.A. in philosophy, he decided to discontinue his
                        studies and become a professional writer.
                        He published his first work in Al Majalla Al Jadida, a magazine started by Salama Musa in
                        1929.Mahfouz then worked as a journalist for Arrissalah, and contributed short stories to
                        Al-Hilal and Al-Ahram.

                    </h1>
                    <p>
                        Civil service
                    </p>
                    <h1>
                        After receiving his bachelor's degree in philosophy from Cairo University in 1934, Mahfouz
                        joined the Egyptian civil service, where he continued to work in various positions and
                        ministries until retirement in 1971.
                        He served first as a clerk at Cairo University, then, in 1938, in the Ministry of Islamic
                        Endowments (Awqaf) as parliamentary secretary to the Minister of Islamic Endowments.
                        In 1945, he requested a transfer to the al-Ghuri Mausoleum library, where he interviewed
                        residents of his childhood neighborhood as part of the "Good Loans Project".
                        [9] In the 1950s, he worked as Director of Censorship in the Bureau of Arts, as Director of
                        the Foundation for the Support of the Cinema, and finally as a consultant to the Ministry of
                        Culture.
                    </h1>
                    <p>
                        Writing career
                    </p>
                    <h1>
                        Mahfouz published 34 novels, over 350 short stories, dozens of screenplays, and five plays
                        over a 70-year career.
                        Possibly his most famous work, The Cairo Trilogy, depicts the lives of three generations of
                        different families in Cairo from World War I until after the 1952 military coup that
                        overthrew King Farouk.
                        He was a board member of the publisher Dar el-Ma'aref.
                        Many of his novels were serialized in Al-Ahram, and his writings also appeared in his weekly
                        column, "Point of View".
                        Before the Nobel Prize only a few of his novels had appeared in the West.
                    </h1>

                    <p>
                        Political influence
                    </p>
                    <h1>
                        Most of Mahfouz's writings deal mainly with politics, a fact he acknowledged: "In all my
                        writings, you will find politics.
                        You may find a story which ignores love or any other subject, but not politics; it is the
                        very axis of our thinking".
                        He espoused Egyptian nationalism in many of his works, and expressed sympathies for the
                        post-World-War-era Wafd Party.
                        [6] He was also attracted to socialist and democratic ideals early in his youth. The
                        influence of socialist ideals is strongly reflected in his first two novels, Al-Khalili and
                        New Cairo, as well as many of his later works.
                        Parallel to his sympathy for socialism and democracy was his antipathy towards Islamic
                        extremism.
                        In his youth, Mahfouz had personally known Sayyid Qutb when Qutb was showing a greater
                        interest in literary criticism than in Islamic fundamentalism; Qutb later became a
                        significant influence on the Muslim Brotherhood.
                        In the mid-1940s, Qutb was one of the first critics to recognize Mahfouz's talent, and by
                        the 1960s, near the end of Qutb's life, Mahfouz even visited him in the hospital.
                        But later, in the semi-autobiographical novel Mirrors, Mahfouz drew a negative portrait of
                        Qutb.
                        He was disillusioned with the 1952 revolution and by Egypt's defeat in the 1967 Six-Day War.
                        He had supported the principles of the revolution, but became disenchanted, saying that the
                        practices failed to live up to the original ideals.
                        Mahfouz's writing influenced a new generation of Egyptian lawyers, including Nabil Mounir
                        and Reda Aslan.

